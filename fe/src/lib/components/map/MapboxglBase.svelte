<script>
	import geoViewport from '@mapbox/geo-viewport';
	import {setupResizeObserver} from '@svizzle/ui';
	import mapboxgl from 'mapbox-gl';
	import {beforeUpdate, onMount, createEventDispatcher} from 'svelte';

	import {_bbox_WS_EN, _bbox_WSEN, _zoom} from '$lib/stores/selection';
	import {clearHero} from '$lib/stores/interaction';

	import {
		FIT_PADDING,
		MAPBOXGL_MAX_ZOOM,
		MAPBOXGL_MIN_ZOOM,
		MAPBOXGL_TILE_SIZE
	} from './consts';
	import {CustomControl} from './util';

	const dispatch = createEventDispatcher();

	export let accessToken = null;
	export let CustomLayers = null;
	export let getLonLat;
	export let items = [];
	export let styleURL;
	export let withScaleControl = true;
	export let withZoomControl = true;
	export let bounds;
	export let customControl;

	/* props */

	let doesMapExist;
	let height = 0;
	let isInteractive = true; // has to be true initially
	let map;
	let mapcontainer;
	let projectFn;
	let width = 0;

	/* bbox */

	$: viewport = geoViewport.viewport(
		$_bbox_WSEN,
		[width, height],
		MAPBOXGL_MIN_ZOOM,
		MAPBOXGL_MAX_ZOOM,
		MAPBOXGL_TILE_SIZE
	);

	/* layers */

	let customLayers;

	$: projectFn && customLayers && customLayers.$set({projectFn});
	$: getLonLat && height && items && width && customLayers &&
		customLayers.$set({
			getLonLat,
			height,
			items,
			width,
		});

	/* controls */
	const addAttributionControl = () => {
		map.addControl(
			new mapboxgl.AttributionControl({
				compact: true
			})
		)
	};

	const addScaleControl = () => {
		map.addControl(
			new mapboxgl.ScaleControl({
				maxWidth: 80,
				unit: 'metric'
			}),
			'bottom-right'
		);
	};

	const addZoomControl = () => {
		map?.addControl(
			new mapboxgl.NavigationControl({
				showCompass: false
			}),
			'bottom-left'
		);
	};

	const addControls = () => {
		addAttributionControl();

		if (withScaleControl) {
			addScaleControl();
		}

		if (withZoomControl) {
			addZoomControl();
		}

		if (customControl) {
			const control = new CustomControl(customControl.control);
			map?.addControl(control, customControl.position);
		}
	};

	/* bbox */

	const fitToBbox = bbox_WSEN => {
		map?.fitBounds(bbox_WSEN, {
			linear: true,
			padding: {
				bottom: FIT_PADDING,
				left: FIT_PADDING,
				right: FIT_PADDING,
				top: FIT_PADDING,
			}
		});
	};

	/* custom layers */

	const addCustomLayers = () => {
		if (CustomLayers) {
			const canvasContainer = map.getCanvasContainer();

			customLayers = new CustomLayers({
				target: canvasContainer,
				props: {
					getLonLat,
					height,
					items,
					map,
					projectFn: x => map.project(x),
					width,
				}
			});
		}
	};

	/* events */

	const updateBbox = () => {
		if (map) {
			const bounds = map.getBounds().toArray();
			_bbox_WS_EN.set(bounds);
		}
	}

	const updateProjection = () => {
		if (map) {
			projectFn = x => map.project(x);
		}
	};
	const updateZoom = () => {
		if (map) {
			const zoom = map.getZoom();
			_zoom.set(zoom);
		}
	}

	const setMapEvents = () => {
		map.on('move', event => {
			updateProjection();
			updateBbox();
			if (event.originalEvent) {
				dispatch('bboxChanged');
			}
		});
		map.on('zoom', () => {
			updateZoom();
		});
	}

	const {
		_writable: _size,
		resizeObserver
	} = setupResizeObserver();

	/* methods */

	// FIXME TBD: bind instead?
	const setGeometry = () => {
		if (!mapcontainer) {
			return
		}

		const elementGeometry = getComputedStyle(mapcontainer);
		width = parseFloat(elementGeometry.width);
		height = parseFloat(elementGeometry.height);
	};

	const createMap = () => {
		const {center, zoom} = viewport;

		mapboxgl.accessToken = accessToken;

		map = new mapboxgl.Map({
			container: mapcontainer,
			center,
			// projection: 'equalEarth',
			renderWorldCopies: false,
			style: styleURL,
			zoom,

			// interactions
			attributionControl: false, // we add this later to have it compact
			doubleClickZoom: isInteractive,
			dragPan: isInteractive,
			dragRotate: false,
			pitchWithRotate: false, // don't render dots in perspective
			scrollZoom: isInteractive,
			touchPitch: false,
			touchZoomRotate: isInteractive,
		})
		.on('load', () => {
			addCustomLayers();
			setMapEvents();
			fitToBbox($_bbox_WSEN);

			setGeometry(); // ipad FIXME: initial svg is 100x100
		})
		.on('click', () => {
			clearHero();
		});

		map.touchZoomRotate.disableRotation();

		// controls

		addControls();
	};

	/* lifecycle */

	beforeUpdate(() => {
		if (viewport && height > 0 && !doesMapExist) {
			doesMapExist = true;
			createMap();
		}
	})

	onMount(() => {
		setGeometry();
	});

	// eslint-disable-next-line no-unused-expressions, no-sequences
	$: $_size, map?.resize()

	$: console.log(bounds);
	$: bounds && fitToBbox(bounds);
</script>

<svelte:head>
	<link
		href='/css/mapbox-gl.css'
		rel='stylesheet'
		type='text/css'
	>
</svelte:head>

<svelte:window on:resize={setGeometry} />

<div class='MapboxglBase'>
	<div
		bind:this={mapcontainer}
		class='mapcontainer'
		use:resizeObserver
	></div>
</div>

<style>
	.MapboxglBase {
		height: 100%;
		position: relative;
		width: 100%;
	}

	.mapcontainer {
		height: 100%;
		width: 100%;
	}
</style>
